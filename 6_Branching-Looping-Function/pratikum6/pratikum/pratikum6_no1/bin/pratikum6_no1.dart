
// Terdapat sebuah nilai 
// a. Jika nilai >70 : return A,
// b. Jika nilai >40 : return B,
// c. Jika nilai>0 : return C,
// d. Selain itu return teks kosong .
void main(){
  var nilai = 0;
  if (nilai > 70){ //jika nilai > 70 : return A
    print('A');

  }else if (nilai > 40){ //jika nilai > 40 : return B
    print('B');

  }else if (nilai > 0){
    print('C'); //jika nilai > 0 : return C 
  
  }else {
    print('');
    //selain dari itu (0) return text kosong 
  }
}
